package pharos.eht.autoClaim.vo;

public class ConditionVO {

	private String conditionCode;
	private String conditionName;
	private String conditionAmout;
	private String conditionInfo;
	public String getConditionCode() {
		return conditionCode;
	}
	public void setConditionCode(String conditionCode) {
		this.conditionCode = conditionCode;
	}
	public String getConditionName() {
		return conditionName;
	}
	public void setConditionName(String conditionName) {
		this.conditionName = conditionName;
	}
	public String getConditionAmout() {
		return conditionAmout;
	}
	public void setConditionAmout(String conditionAmout) {
		this.conditionAmout = conditionAmout;
	}
	public String getConditionInfo() {
		return conditionInfo;
	}
	public void setConditionInfo(String conditionInfo) {
		this.conditionInfo = conditionInfo;
	}
	
	
}
